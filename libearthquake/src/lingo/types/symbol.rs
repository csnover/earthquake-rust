newtype_num! {
    #[derive(Debug)]
    pub struct Symbol(i16);
}

macro_rules! symbols {
    ($($name:ident = $value:literal),* $(,)?) => {
        #[allow(non_upper_case_globals)]
        impl Symbol {
            $(pub const $name: Self = Self($value);)*
        }
    }
}

